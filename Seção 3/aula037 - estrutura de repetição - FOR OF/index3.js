// Iterando utilizando FOR OF
// FOR OF -> ele lê cada índice e pega o valor, sem a necessidade de colocarmos índice. Ele retorna o valor direto.

const nome = 'Luiz Otávio';

for (let valor of nome) { // Quando utilizamos FOR OF ele vai retornar o valor direto de cada índice da string
    console.log(valor) // Executando, ele vai mostrar os valores sem a necessidade de colocarmos índice.
}

// Resultado: L u i z  O t á v i o