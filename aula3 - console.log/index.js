/* O ponto e vírgula no JavaScript é opcional */
console.log('Olá mundo'); //Tipo de dado: STRING (texto)
console.log("Olá mundo"); //Tipo de dado: STRING (texto)
console.log(`Olá mundo`); //Tipo de dado: STRING (texto)


// Também podemos exibir números
console.log(1234); //Número inteiro - Tipo de dado: NUMBER (número)
console.log(15.85); //Número real (ponto flutuante)
console.log(1234, 15.85, 'Luiz Otávio');