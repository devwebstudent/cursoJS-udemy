// Outra coisa que podemos fazer é criar o objeto através do contrutor de objetos


// Criando objeto através do construtor de objetos - new Object()
const pessoa1 = new Object();
pessoa1.nome = 'Luiz'
pessoa1.sobrenome = 'Otávio'

console.log(pessoa1.nome) // Luiz
console.log(pessoa1.sobrenome) // Otávio




// Criando objeto de forma literal
const pessoa2 = {
    nome: 'Luiz',
    sobrenome: 'Otávio'
}

console.log(pessoa2.nome) // Luiz
console.log(pessoa2.sobrenome) // Otávio