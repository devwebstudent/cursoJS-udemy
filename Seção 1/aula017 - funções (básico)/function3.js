// MAIS MODOS DE CRIAR FUNÇÕES - Arrow function -> É uma maneira mais moderna de criar funções

// Fica assim:

const raiz = n => n ** 0.5;

console.log(raiz(9));
console.log(raiz(25));
console.log(raiz(81));


/* As Arrow function vieram para facilitar nossa vida:

Quando temos apenas uma linha da nossa função, podemos eliminar as chaves e os ;
Também podemos eliminar o return
Quando temos apenas um parâmetro na função, também podemos eliminar os ()


*/